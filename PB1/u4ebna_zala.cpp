

/*Учебна зала има правоъгълен размер l на w метра, без колони във вътрешността си.
Залата е разделена на две части – лява и дясна, с коридор - приблизително по средата.
В лявата и в дясната част има редици с бюра.В задната част на залата има голяма входна врата.
В предната част на залата има катедра с подиум за преподавателя.Едно работно
място заема 70 на 120 cm(маса с размер 70 на 40 cm + място за стол и преминаване 
	с размер 70 на 80 cm).Коридорът е широк поне 100 cm.Изчислено е, че заради
	входната врата(която е с отвор 160 cm) се губи точно 1 работно място, а заради
	катедрата(която е с размер 160 на 120 cm) се губят точно 2 работни места.
	Напишете програма, която въвежда размери на учебната зала и изчислява броя 
	работни места в нея при описаното разположение(вж.фигурата).

Входни данни
От конзолата се четат 2 числа, по едно на ред : l(дължина в метри) и w(широчина в метри).

Ограничения : 3 ≤ w ≤ l ≤ 100.

Изходни данни
Да се отпечата на конзолата едно цяло число : броят места в учебната зала.*/


#include <iostream>
using namespace std;

int main()
{
	double l, w;
	cin >> l >> w;

	double a1 = l * 100;
	double a2 = w * 100;

	int cols = ((int)a2 - 100) / 70;
	int rows = (int)a1/ 120;
	int seats = (rows * cols) - 3;

	cout << seats << endl;
	return 0;
}
